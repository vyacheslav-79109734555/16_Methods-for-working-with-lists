shop = [['каретка', 1200], ['шатун', 1000], ['седло', 300],
        ['педаль', 100], ['седло', 1500], ['рама', 12000],
        ['обод', 2000], ['шатун', 200], ['седло', 2700]]

text = input('Название детали: ')
x = 0
y = 0

for arr in shop:
    if arr[0] == text:
        x += 1
        y += arr[1]

print('Кол-во деталей -', x)
print('Общая стоимость -', y)
